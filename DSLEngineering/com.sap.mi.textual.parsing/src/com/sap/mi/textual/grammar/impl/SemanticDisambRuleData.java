package com.sap.mi.textual.grammar.impl;

public class SemanticDisambRuleData {
	
	private String rule;
	private String ocl;
	public SemanticDisambRuleData(String rule, String ocl) {
		super();
		this.rule = rule;
		this.ocl = ocl;
	}
	public String getRule() {
		return rule;
	}
	public String getOcl() {
		return ocl;
	}
	

}
